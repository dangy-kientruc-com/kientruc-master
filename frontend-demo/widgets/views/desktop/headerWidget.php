<script type="text/javascript">
    async function showNotify() {
        await $('body').find('.box-notify').removeClass('visibility');
        await $('body').find('.item1').css({'left': '0'});
        $('body').addClass('over-hidden');
        return false;
    }

    async function showItem2(json) {
        if (json.choi !== undefined) {
            //tim item
            var li = await $('body').find('li[data-choise=' + json.choi + ']');
            await li.siblings().removeClass('active');
            await li.addClass('active');
        }

        if (json.item !== undefined) {
            var its = await $('.item2').find('div[data-item=' + json.item + ']');
            await its.siblings().addClass('d-none');
            await its.removeClass('d-none');
        }

        if (json.one !== undefined) {
            await $('body').find('.children').each(function () {
                $(this).addClass('d-none');
            });
            $('body').find('.part-0' + json.one).removeClass('d-none');
        } else {
            $('body').find('.children').each(function () {
                $(this).removeClass('d-none');
            });
        }

        var height = await window.innerHeight - 82;
        await $('body').find('.autoBoxHeight').each(function () {
            $(this).css({'height': height + 'px'});
        });
        await $('.box-scroll-notification').slimScroll({height: 'auto'});

        $('body').find('.item2').css({'left': '300px'});
        return false;
    }

    function showItem3() {
        $('body').find('.item3').css({'left': '620px'});
        return false;
    }

    async function offNotify() {
        await $('body').find('.item3').css({'left': '-420px'});
        await $('body').find('.item2').css({'left': '-320px'});
        await $('body').find('.item1').css({'left': '-300px'});
        await $('body').find('.box-notify').addClass('visibility');
        $('body').removeClass('over-hidden');
        return false;
    }

    async function catchLink() {
        var hash = window.location.hash;
        switch (hash) {
            case '#step1':
                await showNotify();
                await showItem2({choi: 'ask', item: 5});
                break;
        }
        //console.log(hash);
    }
</script>

